package loop;

public class loop4 {

	public static void main(String[] args) {
		for(int i=0;i<3;i++) {
			System.out.print("A");
			for(int j=0;j<1;j++) {
				System.out.print("B");
			}for(int j=0;j<1;j++) {
				System.out.print("C");
			}for(int j=0;j<1;j++) {
				System.out.print("D");
			}
			System.out.println(" ");	
			
		}
		
	}

}
