package loop;

public class loop6 {

	public static void main(String[] args) {
		
		for(int i=0;i<3;i++) {
			System.out.print("a");
			for(int j=0;j<1;j++) {
				System.out.print("a");
			}for(int j=0;j<1;j++) {
				System.out.print("b");
			}for(int j=0;j<1;j++) {
				System.out.print("b");
			}
			System.out.println(" ");	
        
		
		
		}
		
		
		
	}

}
